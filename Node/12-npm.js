/*
How to use third party module/package/library in our application
Preinstalled: Node and npm
Node: https://nodejs.org/en/download/ (node + npm)

npm : https://www.npmjs.com/

Check the installation of the node and npm
Open the command prompt
node -v

install npm packages (locally: installed package available only for this application)
npm install <packagename>
npm i <packagename>

install npm packages (globally: installed package available in all the application which we are creating)
npm install -g <packagename>

install npm packages as devDependencies
npm install <packagename> -D
npm install <packagename> --save-dev

install npm package of specific version
npm install <packagename>@version
Ex:
npm install bootstrap@5.1.2

Uninstall the npm package
npm uninstall <packagename>

package.json
1. Create package.json file manually and add the required properties
2. npm init (create package.json)
3. npm init -y

*/